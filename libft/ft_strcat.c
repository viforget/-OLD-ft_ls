#include "libft.h"

char	*ft_strcat(char *s1, const char *s2)
{
	int i;
	int n;

	n = 0;
	i = 0;
	while (s1[i] != '\0')
		i++;
	while (s2[n] != '\0')
	{
		s1[i + n] = s2[n];
		n++;
	}
	s1[i + n] = '\0';
	return (s1);
}
