#include "libft.h"

void	ft_tabdel(char **tab, size_t j)
{
	size_t	i;

	i = 0;
	if (tab != NULL)
	{
		while (i < j)
		{
			ft_strdel(&tab[i]);
			i++;
		}
		ft_memdel((void **)&tab);
	}
}
